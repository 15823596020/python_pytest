# 被测代码：定义一个计算器类
class Calc:
    def add(self, a, b):  # 定义一个加法方法
        return a + b  # 返回a+b的值

    def sub(self, a, b):  # 定义一个减法方法
        return a - b  # 返回a/b的值

    def mul(self, a, b):  # 定义一个乘法方法
        return a * b  # 返回a/b的值

    def div(self, a, b):  # 定义一个除法方法
        return a / b  # 返回a/b的值

